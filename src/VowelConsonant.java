public class VowelConsonant {
    public static void main(String[] args) {
        char Character = 'D';

        if (Character == 'A' || Character == 'E' || Character == 'I' || Character =='O' || Character == 'U' ){
            System.out.println("Vowel");
        }
        else {
            System.out.println("Consonant");
        }

    }
}
